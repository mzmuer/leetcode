func uniquePathsWithObstacles(obstacleGrid [][]int) int {
	n, m := len(obstacleGrid), len(obstacleGrid[0])
	f := make([][]int, n)
	for i := 0; i < n; i++ {
		f[i] = make([]int, m)
	}

	if obstacleGrid[0][0] == 0 {
		f[0][0] = 1
	}

	for i := 0; i < n; i++ {
		for j := 0; j < m; j++ {
			if obstacleGrid[i][j] != 0 {
				f[i][j] = 0
				continue
			}

			if i-1 >= 0 && obstacleGrid[i-1][j] == 0 {
				f[i][j] += f[i-1][j]
			}

			if j-1 >= 0 && obstacleGrid[i][j-1] == 0 {
				f[i][j] += f[i][j-1]
			}
		}
	}

	return f[n-1][m-1]
}
